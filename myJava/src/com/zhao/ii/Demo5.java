/**
 * 二进制
 */
package com.zhao.ii;

public class Demo5 {
	public static void main(String[] args) {
		System.out.println(~20);// 加一加负
		System.out.println(~-20);
		System.out.println(2 & 5);
		System.out.println(2 | 5);
		System.out.println(-2 & -5);
		
		System.out.println(-200>>30);
		System.out.println(1>>>2);
		System.out.println(1<<2);
	}
}